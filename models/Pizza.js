const { Schema, model } = require('mongoose');

const PizzaSchema = new Schema({
    pizzaName: {
        type: String
    },
    createdBy: {
        type: String
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    size: {
        type: String,
        default: 'Large'
    },
    toppings: []
});

// Create the Pizzal model using the PizzaSchema
const Pizza = model('Pizza', PizzaSchema);

// export the Pizza mode
module.exports = Pizza;